// Get language from localStorage or return default
export const getStoredLanguage = (): Language => {
  if (typeof window !== 'undefined') {
    const stored = localStorage.getItem('hbike_language')
    if (stored && ['ja', 'en', 'vi'].includes(stored)) {
      return stored as Language
    }
  }
  return DEFAULT_LANGUAGE
}

// Save language to localStorage
export const setStoredLanguage = (lang: Language): void => {
  if (typeof window !== 'undefined') {
    localStorage.setItem('hbike_language', lang)
  }
}

export const useTranslation = (lang: Language = DEFAULT_LANGUAGE) => {
  return (key: string): string => {
    const keys = key.split('.')
    let value: any = translations[lang]
    
    for (const k of keys) {
      value = value?.[k]
    }
    
    return value || key
  }
}
